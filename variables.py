""" Palabras reservadas de python
Las palabras reservadas, como su nombre lo indican, son palabras las cuales el lenguaje de
programación ya ha reservado para realizar ciertas tareas.

Nosotros no podemos declarar una variable con el mismo nombre que una palabra reservada

En python el listado de palabras reservadas es el siguiente:
False	class	is	return
None	continue	lambda	try
True	def	nonlocal	while
and	del	not	with
as	elif	or	yield
assert	else	pass	*
break	except	raise	*
En el curso trabajaremos con la mayoría de las palabras reservadas, sin embargo,
si quieres conocer todas ellas, puedes consultar el siguiente link:
https://www.programiz.com/python-programming/keyword-list """
